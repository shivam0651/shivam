package com.csi.arithmaticopraters;

public class Arithmaticopraters {
	public static void main(String[] args) {

		int a = 20, b = 10, result = 0;
		result = a - b;

		System.out.println("Substraction\n" + result);
		result = a * b;
		System.out.println("multiplication\n" + result);
		result = a / b;
		System.out.println("division\n" + result);
	}

	static {
		int p = 20, q = 10, result = 0;
		result = p + q;
		System.out.println("addition of \n" + result);
	}
}
